def resnet50(opt=None, weight_dir=None):
    """Constructs a ResNet-50 model.
    """
    model = ResNet(Bottleneck, [3, 4, 6, 3], num_classes=opt.action_classes, shortcut_type=opt.resnet_shortcut,
                   frame_size=opt.action_frame_size, sample_duration=opt.sample_duration)
    
    checkpoint = torch.load(weight_dir, map_location=lambda storage, loc: storage)
    state_dict = {str.replace(k, 'module.', ''): v for k, v in checkpoint['state_dict'].items()}
    model.load_state_dict(state_dict)

    return model

def I3D(opt=None, weight_dir=None):
    """Constructs a I3D model.
    """
    model = InceptionI3d(num_classes=opt.action_classes, in_channels=3)
    model_dict = model.state_dict()
    checkpoint = torch.load(weight_dir, map_location=lambda storage, loc: storage)
    state_dict = {str.replace(k, 'module.', ''): v for k, v in checkpoint.items()}
    model_dict.update(state_dict)
    model.load_state_dict(model_dict)

    return model

def Action_Detector(opt=None):
    if opt.action_base_model == 'resnet50':
        weight_dir = './weights/resnet-50-kinetics.pth'
        model = resnet50(opt, weight_dir)
    elif opt.action_base_model == 'I3D':
        weight_dir = './weights/rgb_imagenet.pt'
        model = I3D(opt, weight_dir)
    else:
        print('Error, no such model')

    return model
